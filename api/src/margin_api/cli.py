_FOREIGN_SKIPS_PATH = Path(__file__).resolve().parents[3] / "engine" / "foreign_skips.yaml"


def _load_foreign_skips() -> set[str]:
    """Load the set of known foreign tickers to skip during seed."""
    import yaml

    if not _FOREIGN_SKIPS_PATH.exists():
        return set()
    with open(_FOREIGN_SKIPS_PATH) as f:
        data = yaml.safe_load(f) or {}
    # Cast to str to avoid YAML boolean gotcha (ON -> True, NO -> False)
    return {str(t) for t in data.get("tickers", [])}


def _save_foreign_skips(skips: set[str]) -> None:
    """Persist the foreign skip list to YAML."""
    lines = [
        "# Auto-generated by seed — tickers rejected by country guard",
        f"# Last updated: {datetime.now(UTC).strftime('%Y-%m-%dT%H:%M:%SZ')}",
        f"# {len(skips)} foreign-domiciled tickers",
        "",
        "tickers:",
    ]
    for ticker in sorted(skips):
        lines.append(f'  - "{ticker}"')
    lines.append("")
    _FOREIGN_SKIPS_PATH.write_text("\n".join(lines))
    logger.info("Saved %d foreign tickers to %s", len(skips), _FOREIGN_SKIPS_PATH)


