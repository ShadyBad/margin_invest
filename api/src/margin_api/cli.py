async def seed_ticker_data(
    *,
    ticker: str,
    provider: YFinanceProvider,
    session,
    fallback_provider=None,
) -> SeedResult:
    """Fetch data for a single ticker and upsert it into the database.

    Returns a :class:`SeedResult` with status "ok", "partial", "foreign",
    "skipped", or "failed".
    """
    try:
        # Fetch all data categories via provider.fetch_all
        # Use 5 years of price data so years_of_history passes liquidity filter (threshold=5)
        results = provider.fetch_all(ticker, price_days=1825)

        # Attempt fallback for failed categories
        if fallback_provider is not None:
            fallback_map = {
                "fundamentals": "fetch_fundamentals",
                "price": "fetch_price_history",
                "earnings": "fetch_earnings",
            }
            for cat_name, method_name in fallback_map.items():
                if cat_name in results and not results[cat_name].success:
                    try:
                        method = getattr(fallback_provider, method_name)
                        fb_result = method(ticker)
                        if fb_result.success:
                            results[cat_name] = fb_result
                            logger.info(
                                "  %s: %s rescued by %s",
                                ticker,
                                cat_name,
                                fb_result.provider_name,
                            )
                    except Exception as fb_exc:
                        logger.warning(
                            "  %s: fallback %s failed: %s",
                            ticker,
                            cat_name,
                            fb_exc,
                        )

        # Track per-category success/failure
        categories_succeeded: list[str] = []
        categories_failed: list[str] = []

        fundamentals = results["fundamentals"]
        price_history = results["price"]
        earnings = results["earnings"]
        info_result = results["info"]

        for name_key, result in [
            ("fundamentals", fundamentals),
            ("price", price_history),
            ("earnings", earnings),
            ("info", info_result),
        ]:
            if result.success:
                categories_succeeded.append(name_key)
            else:
                categories_failed.append(name_key)

        logger.info(
            "  %s: fundamentals=%s price=%s earnings=%s info=%s",
            ticker,
            "ok" if fundamentals.success else "FAIL",
            "ok" if price_history.success else "FAIL",
            "ok" if earnings.success else "FAIL",
            "ok" if info_result.success else "FAIL",
        )

        # Extract info from the info FetchResult
        info = info_result.raw_data if info_result.success else {}

        name = info.get("shortName") or info.get("longName") or ticker
        raw_sector = info.get("sector", "")
        sector = SECTOR_MAP.get(raw_sector, raw_sector)
        sub_industry = info.get("industry")
        country = info.get("country")
        market_cap = Decimal(str(info.get("marketCap", 0)))
        shares_outstanding = info.get("sharesOutstanding")

        # Reject non-US assets (foreign or unknown country)
        if country != "United States":
            logger.info("  SKIP %s — non-US domicile (%s)", ticker, country or "unknown")
            return SeedResult(status="foreign", error_message=f"Non-US domicile: {country}")

        # Upsert Asset row — uses ON CONFLICT to avoid race conditions
        # between concurrent ingest workers.
        asset_values = dict(
            ticker=ticker,
            name=name,
            sector=sector,
            sub_industry=sub_industry,
            country=country,
            market_cap=market_cap,
            shares_outstanding=shares_outstanding,
        )
        asset_stmt = pg_insert(Asset).values(**asset_values)
        asset_stmt = asset_stmt.on_conflict_do_update(
            index_elements=["ticker"],
            set_={
                "name": asset_stmt.excluded.name,
                "sector": asset_stmt.excluded.sector,
                "sub_industry": asset_stmt.excluded.sub_industry,
                "country": asset_stmt.excluded.country,
                "market_cap": asset_stmt.excluded.market_cap,
                "shares_outstanding": asset_stmt.excluded.shares_outstanding,
            },
        ).returning(Asset.id)
        result = await session.execute(asset_stmt)
        asset_id = result.scalar_one()

        # Build financial data
        today_iso = datetime.now(UTC).strftime("%Y-%m-%d")
        now_utc = datetime.now(UTC)

        price_data = _sanitize_for_json(price_history.raw_data if price_history.success else None)
        earnings_data = _sanitize_for_json(earnings.raw_data if earnings.success else None)

        # Extract multi-year periods if available, otherwise fall back to single-row
        periods_raw = fundamentals.raw_data.get("periods") if fundamentals.success else None

        if periods_raw:
            # Store one FinancialData row per fiscal year
            for idx, period_dict in enumerate(periods_raw):
                period_end = period_dict.get("period_end", today_iso)
                income_statement = _sanitize_for_json(period_dict.get("income_statement"))
                balance_sheet = _sanitize_for_json(period_dict.get("balance_sheet"))
                cash_flow = _sanitize_for_json(period_dict.get("cash_flow"))

                # Only the most recent period gets price_history and earnings_data
                is_latest = idx == len(periods_raw) - 1

                fd_values = dict(
                    asset_id=asset_id,
                    period_end=period_end,
                    filing_date=period_end,
                    income_statement=income_statement,
                    balance_sheet=balance_sheet,
                    cash_flow=cash_flow,
                    price_history=price_data if is_latest else None,
                    earnings_data=earnings_data if is_latest else None,
                    source="yfinance",
                    fetched_at=now_utc,
                )
                fd_stmt = pg_insert(FinancialData).values(**fd_values)
                fd_stmt = fd_stmt.on_conflict_do_update(
                    constraint="uq_financial_data_asset_period",
                    set_={
                        "income_statement": fd_stmt.excluded.income_statement,
                        "balance_sheet": fd_stmt.excluded.balance_sheet,
                        "cash_flow": fd_stmt.excluded.cash_flow,
                        "price_history": fd_stmt.excluded.price_history,
                        "earnings_data": fd_stmt.excluded.earnings_data,
                        "fetched_at": fd_stmt.excluded.fetched_at,
                    },
                )
                await session.execute(fd_stmt)
        else:
            # Fallback: single-row behavior (no "periods" key from provider)
            income_statement = _sanitize_for_json(
                fundamentals.raw_data.get("income_statement") if fundamentals.success else None
            )
            balance_sheet = _sanitize_for_json(
                fundamentals.raw_data.get("balance_sheet") if fundamentals.success else None
            )
            cash_flow = _sanitize_for_json(
                fundamentals.raw_data.get("cash_flow") if fundamentals.success else None
            )

            fd_values = dict(
                asset_id=asset_id,
                period_end=today_iso,
                filing_date=today_iso,
                income_statement=income_statement,
                balance_sheet=balance_sheet,
                cash_flow=cash_flow,
                price_history=price_data,
                earnings_data=earnings_data,
                source="yfinance",
                fetched_at=now_utc,
            )
            fd_stmt = pg_insert(FinancialData).values(**fd_values)
            fd_stmt = fd_stmt.on_conflict_do_update(
                constraint="uq_financial_data_asset_period",
                set_={
                    "income_statement": fd_stmt.excluded.income_statement,
                    "balance_sheet": fd_stmt.excluded.balance_sheet,
                    "cash_flow": fd_stmt.excluded.cash_flow,
                    "price_history": fd_stmt.excluded.price_history,
                    "earnings_data": fd_stmt.excluded.earnings_data,
                    "fetched_at": fd_stmt.excluded.fetched_at,
                },
            )
            await session.execute(fd_stmt)

        await session.commit()

        # Reset failure tracking on success
        asset_row = await session.execute(select(Asset).where(Asset.id == asset_id))
        asset_obj = asset_row.scalar_one()
        await update_failure_status(session, asset_obj, "success", None)

        # Determine status: "ok" if all succeeded, "partial" if some failed
        status = "ok" if not categories_failed else "partial"
        return SeedResult(
            status=status,
            categories_succeeded=categories_succeeded,
            categories_failed=categories_failed,
        )

    except Exception as exc:
        logger.exception("Failed to seed data for %s", ticker)
        await session.rollback()

        # Classify error and update failure tracking
        error_type = classify_error(exc)
        try:
            asset_row = await session.execute(select(Asset).where(Asset.ticker == ticker))
            asset_obj = asset_row.scalar_one_or_none()
            if asset_obj is not None:
                await update_failure_status(session, asset_obj, error_type, str(exc))
        except Exception:
            logger.warning("Could not update failure status for %s", ticker)

        return SeedResult(
            status="failed",
            error_message=str(exc),
        )


async def _get_universe_tickers() -> list[str]:
    """Read ticker list from the active universe snapshot."""
    engine = get_engine()
    session_factory = get_session_factory(engine)
    async with session_factory() as session:
        snapshot = await get_active_snapshot(session)
    if snapshot is None:
        logger.error("No active universe snapshot. Run 'universe activate' first.")
        logger.error("Or use --tickers to seed a specific subset.")
        sys.exit(1)
    logger.info("Using universe v%s (%d tickers)", snapshot.version, snapshot.ticker_count)
    return list(snapshot.tickers)


async def run_seed(tickers: list[str] | None = None) -> None:
    """Seed financial data for all (or specified) tickers.

    Creates the provider with built-in rate limiting, then iterates
    through tickers and calls :func:`seed_ticker_data` for each one.

    Loads and updates the foreign-ticker skip list so known foreign
    tickers are skipped immediately on subsequent runs.
    """
    if tickers is None:
        tickers = await _get_universe_tickers()

    # Load known foreign tickers from skip list
    foreign_skips = _load_foreign_skips()
    if foreign_skips:
        before = len(tickers)
        tickers = [t for t in tickers if t not in foreign_skips]
        skipped = before - len(tickers)
        if skipped:
            logger.info("Skipped %d known foreign tickers from skip list", skipped)

    # 12 tickers/min => ~60 actual API requests/min (fetch_all makes ~5 calls)
    limiter = RateLimiter(requests_per_minute=12)
    provider = YFinanceProvider(rate_limiter=limiter)

    # Construct FMP fallback provider if API key is available
    fmp_provider = None
    fmp_key = os.environ.get("FMP_API_KEY")
    if fmp_key:
        from margin_engine.ingestion.providers.fmp_provider import FMPProvider

        fmp_provider = FMPProvider(api_key=fmp_key)
        logger.info("FMP fallback provider enabled")

    engine = get_engine()
    session_factory = get_session_factory(engine)

    successes = 0
    failures = 0
    foreign_count = 0
    total = len(tickers)

    for i, ticker in enumerate(tickers, start=1):
        logger.info("[%d/%d] Seeding %s...", i, total, ticker)

        # Check if ticker should be ingested (skip quarantined/permanently_skipped)
        async with session_factory() as session:
            asset_check = await session.execute(select(Asset).where(Asset.ticker == ticker))
            existing_asset = asset_check.scalar_one_or_none()
            if existing_asset and not should_ingest_ticker(
                existing_asset.ingestion_status,
                existing_asset.consecutive_failures,
                existing_asset.last_retry_at,
            ):
                logger.info("  %s SKIPPED (status=%s)", ticker, existing_asset.ingestion_status)
                continue

        # Resume check: skip if already seeded today (by fetched_at timestamp)
        async with session_factory() as session:
            start_of_today = datetime.now(UTC).replace(hour=0, minute=0, second=0, microsecond=0)
            resume_check = await session.execute(
                select(FinancialData)
                .join(Asset, FinancialData.asset_id == Asset.id)
                .where(Asset.ticker == ticker, FinancialData.fetched_at >= start_of_today)
                .limit(1)
            )
            if resume_check.scalar_one_or_none() is not None:
                logger.info("  %s SKIPPED (already seeded today)", ticker)
                continue

        async with session_factory() as session:
            result = await seed_ticker_data(
                ticker=ticker,
                provider=provider,
                session=session,
                fallback_provider=fmp_provider,
            )

        if result.status == "ok":
            successes += 1
            logger.info("  %s OK", ticker)
        elif result.status == "partial":
            successes += 1
            logger.info(
                "  %s PARTIAL (failed: %s)",
                ticker,
                ", ".join(result.categories_failed),
            )
        elif result.status == "foreign":
            foreign_count += 1
            foreign_skips.add(ticker)
        elif result.status == "skipped":
            logger.info("  %s SKIPPED", ticker)
        else:
            failures += 1
            logger.error("  %s FAILED: %s", ticker, result.error_message)

    # Persist updated skip list if new foreign tickers were discovered
    if foreign_count > 0:
        _save_foreign_skips(foreign_skips)
        logger.info("Discovered %d new foreign tickers this run", foreign_count)

    logger.info(
        "Seed complete: %d succeeded, %d failed, %d foreign-skipped out of %d tickers",
        successes,
        failures,
        foreign_count,
        total,
    )


